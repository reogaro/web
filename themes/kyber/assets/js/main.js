document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('pre').forEach(pre => {
        let container = pre.parentElement.classList.contains('highlight') ? pre.parentElement : null;

        // If Chroma didn't highlight this language, it won't have the .highlight wrapper.
        // We dynamically wrap it so our CSS and positioning works perfectly.
        if (!container) {
            container = document.createElement('div');
            container.className = 'highlight';
            pre.parentNode.insertBefore(container, pre);
            container.appendChild(pre);
        }

        if (container.querySelector('.copy-code-button')) return;

        const btn = document.createElement('button');
        btn.className = 'copy-code-button';
        btn.innerText = 'Copy';
        btn.setAttribute('aria-label', 'Copy code to clipboard');

        btn.addEventListener('click', () => {
            const code = container.querySelector('code');
            if (code) {
                navigator.clipboard.writeText(code.innerText).then(() => {
                    btn.innerText = 'Copied!';
                    setTimeout(() => btn.innerText = 'Copy', 2000);
                });
            }
        });

        container.appendChild(btn);
    });
});
